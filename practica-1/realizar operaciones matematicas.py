# Programa para realizar operaciones matemáticas
print("Calculadora básica")
num1 = int(input("Ingresa el 1er número: "))
num2 = int(input("Ingresa el 2do número: "))

suma = num1 + num2
resta = num1 - num2
multiplicacion = num1 * num2
division = num1 / num2

print("\nResultados:")
print(f"La suma de {num1} + {num2} es: {suma}")
print(f"La resta de {num1} - {num2} es: {resta}")
print(f"La multiplicación de {num1} * {num2} es: {multiplicacion}")
print(f"La división de {num1} / {num2} es: {division}")
print("\nGracias Por Usar Mi Calculadora")